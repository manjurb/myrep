package com.majnu.spring.springcore.reftypes;

public class Scores 
{
	private int ds;
	private int oops;
	private int c;
	public int getDs() {
		return ds;
	}
	public void setDs(int ds) {
		this.ds = ds;
	}
	public int getOops() {
		return oops;
	}
	public void setOops(int oops) {
		this.oops = oops;
	}
	public int getC() {
		return c;
	}
	public void setC(int c) {
		this.c = c;
	}
}
